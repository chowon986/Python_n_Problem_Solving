# 출력 - 아래에 print문을 작성합니다.
print("Hello World", end ="") 
# , end=""을 하면 이어쓰기 가능
# a 변수를 미리 선언하고 , end=a를 하면 이어서 a가 써짐

b = "test"
print("Hello World", end=" " + b)

a = "Hello World"
print(a)

